import React from 'react';
import { BrowserRouter, Routes, Route } from 'react-router-dom';
import { ChakraProvider } from '@chakra-ui/react';
import { AuthProvider } from './contexts/AuthContext';
import Login from './pages/Auth/Login';
import HomePage from './components/Home';
import Signup from './pages/Auth/Signup';
import Dashboard from './pages/Company/Dashboard';
import CreateAd from './pages/Company/Ads/CreateAd';
import ManageAds from './pages/Company/Ads/ManageAds';
import Analytics from './pages/Company/Analytics';
import InfluencerDashboard from './pages/Influencer/InfluencerDashboard';
import InfluencerProfile from './pages/Influencer/InfluencerProfile';
import InfluencerEarnings from './pages/Influencer/InfluencerEarnings';
import InfluencerMessages from './pages/Influencer/InfluencerMessages';
// import Campaigns from './pages/Influencer/Campaigns/index';
import Messages from './pages/Shared/Messages';
import Profile from './pages/Shared/Profile';
import InfluencerDiscover from './pages/Company/InfluencerDiscover';

const App = () => (
  <ChakraProvider>
    <AuthProvider>
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<HomePage />} />
          <Route path="/login" element={<Login />} />
          <Route path="/signup" element={<Signup />} />
          <Route path="/company/dashboard" element={<Dashboard />} />
          <Route path="/create-ad" element={<CreateAd />} />
          <Route path="/manage-ads" element={<ManageAds />} />
          <Route path="/analytics" element={<Analytics />} />
          <Route path="/company/influencer-discover" element={<InfluencerDiscover />} />
          <Route path="/influencer/dashboard" element={<InfluencerDashboard />} />
          <Route path="/influencer/profile" element={<InfluencerProfile />} />
          <Route path="/influencer/earnings" element={<InfluencerEarnings />} />
          <Route path="/influencer/messages" element={<InfluencerMessages />} />
          <Route path="/influencer/:id" element={<InfluencerDashboard />} />
         
          <Route path="/messages" element={<Messages />} />
          <Route path="/profile" element={<Profile />} />
        </Routes>
      </BrowserRouter>
    </AuthProvider>
  </ChakraProvider>
);

export default App;